CREATE TABLE CONTINENT (
 ID_CONTINENT NUMBER PRIMARY KEY,
 NUME_CONTINENT VARCHAR(100) NOT NULL
);
CREATE TABLE TARA (
 ID_TARA NUMBER PRIMARY KEY,
 NUME_TARA VARCHAR(100) NOT NULL,
 CAPITALA VARCHAR(100),
 AN_ADERARE NUMBER(4)
);
CREATE TABLE PRESEDINTE (
 ID_PRESEDINTE NUMBER PRIMARY KEY,
 NUME_PRESEDINTE VARCHAR(100) NOT NULL,
 INCEPUT_MANDAT DATE,
 SFARSIT_MANDAT DATE
);
CREATE TABLE STATISTICI_ECONOMICE (
 ID_STATISTICI NUMBER PRIMARY KEY,
 PIB NUMBER,
 SOMAJ NUMBER,
 EXPORTURI NUMBER,
 IMPORTURI NUMBER,
 ID_TARA NUMBER,
 FOREIGN KEY (ID_TARA) REFERENCES TARA(ID_TARA)
);
CREATE TABLE POPULATIE (
 ID_POPULATIE NUMBER PRIMARY KEY,
 NUMAR_LOCUITORI NUMBER,
 CRESTERE_ANUALA NUMBER,
 ID_TARA NUMBER,
 FOREIGN KEY (ID_TARA) REFERENCES TARA(ID_TARA)
);
CREATE TABLE LIMBA (
 ID_LIMBA NUMBER PRIMARY KEY,
 LIMBA_VORBITA VARCHAR(50) NOT NULL
);
CREATE TABLE TARA_LIMBA (
 ID_TARA NUMBER,
 ID_LIMBA NUMBER,
 PRIMARY KEY (ID_TARA, ID_LIMBA),
 FOREIGN KEY (ID_TARA) REFERENCES TARA(ID_TARA),
 FOREIGN KEY (ID_LIMBA) REFERENCES LIMBA(ID_LIMBA)
);
